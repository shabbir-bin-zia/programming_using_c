#include<stdio.h>
int main()
{
    int a;
    long int b;
    long long int c;

    float d;

    double e;

    char f;


       scanf("%d", &a);
       printf("Enter int number: %d\n", a);

       scanf("%ld", &b);
       printf("Enter long int number:%ld\n",b);

       scanf("%llu", &c);
       printf("Enter second number: %llu\n",c);


       scanf("%f", &d);
       printf("Enter second number: %f\n",d);

       scanf("%lf", &e);
       printf("Enter second number: %lf\n",e);

       scanf("%c", &f);
       printf("Enter second number: %c\n",f);



}
